package Exercise_05_10;

public class AssignmentOperator {

    public static void main(String[] args) {
        int number1 = 57;
        int number2 = 33;
        System.out.println(number1 += number2);
        System.out.println(number1 -= number2);
        System.out.println(number1 *= number2);
        System.out.println(number1 /= number2);
        System.out.println(number1 %= number2);
    }
}
