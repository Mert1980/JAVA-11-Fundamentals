package Exercise_05_07;

public class LogicalOperators {
    public static void main(String[] args) {
        boolean a = true;
        boolean b= false;

        System.out.println("a: " + a);
        System.out.println("b: " + b);
        System.out.println("a && b: " + (a && b));
        System.out.println("a || b: " + (a || b));
        System.out.println("!a: " + !a);
        System.out.println("!b: " + !b);
    }
}
